import type { ExtraPropertiesEntity } from '@/types/util';

export interface ExperimentResultIndex {
  url: string;
  /*
   * BAI     # BAM index files ( http://samtools.github.io/hts-specs/SAMv1.pdf "BAI" )
   * BGZF    # BGZip index files (often .gzi)
   * CRAI    # CRAM index files ( https://samtools.github.io/hts-specs/CRAMv3.pdf "CRAM index" )
   * CSI     # See http://samtools.github.io/hts-specs/CSIv1.pdf
   * TABIX   # See https://samtools.github.io/hts-specs/tabix.pdf
   * TRIBBLE
   */
  format: 'BAI' | 'BGZF' | 'CRAI' | 'CSI' | 'TABIX' | 'TRIBBLE';
}

export interface ExperimentResult extends ExtraPropertiesEntity {
  id: number; // generated by Katsu
  identifier: string; // specified in JSON - TODO: clean up this mess in Katsu
  description: string;
  filename: string;
  url: string;
  indices: ExperimentResultIndex[];
  genome_assembly_id: string;
  file_format:
    | 'SAM'
    | 'BAM'
    | 'CRAM'
    | 'VCF'
    | 'BCF'
    | 'MAF'
    | 'GVCF'
    | 'BigWig'
    | 'BigBed'
    | 'FASTA'
    | 'FASTQ'
    | 'TAB'
    | 'SRA'
    | 'SRF'
    | 'SFF'
    | 'GFF'
    | 'PDF'
    | 'CSV'
    | 'TSV'
    | 'JPEG'
    | 'PNG'
    | 'GIF'
    | 'HTML'
    | 'MARKDOWN'
    | 'MP3'
    | 'M4A'
    | 'MP4'
    | 'DOCX'
    | 'XLS'
    | 'XLSX'
    | 'UNKNOWN'
    | 'OTHER';
  data_output_type?: 'Raw data' | 'Derived data';
  usage?: string;
  creation_date?: string;
  created_by?: string;
  // Can be added in front end:
  experiment_id?: string;
}
